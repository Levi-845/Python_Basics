# ----------------------------- Logical operators ----------------------------

print((5 > 3) and (3 < 8))  #  and  Returns True if both conditions are true	     (5 > 3) and (3 < 8) →  True
print((5 > 3) or (3 > 8))   #  or   Returns True if at least one condition is true	 (5 > 3) or (3 > 8) →   True
print(not (5 > 3))          #  not	Reverses the boolean value	not                  (5 > 3) →              False