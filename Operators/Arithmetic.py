# ------------------ Arithmetic Oprators ------------------------
print(5 + 3)   #  +	  Addition             5 + 3 → 8
print(5 - 3)   #  -   Subtraction	       5 - 3 → 2
print(5 * 3)   #  *   Multiplication	   5 * 3 → 15
print(5 / 2)   #  /   Division (float)     5 / 2 → 2.5
print(5 // 2)  #  //  Floor Division	   5 // 2 → 2
print(5 % 2)   #  %   Modulus (remainder)  5 % 2 → 1
print(5 ** 2)  #  **  Exponentiation	   5 ** 2 → 25


