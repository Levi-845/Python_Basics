# ------------------------- BitWise operators -------------------------

print(5 & 3)         #   &	   Bitwise AND	  5 & 3 → 1 (0001)
print("Bitwise OR")  #   `	       `          Bitwise OR
print(5 ^ 3)         #   ^	   Bitwise XOR	  5 ^ 3 → 6 (0110)
print(~5)            #   ~	   Bitwise NOT	  ~5 → -6 (2’s complement)
print(5 << 1)        #   <<	   Left Shift	  5 << 1 → 10 (1010)
print(5 >> 1)        #   >>	   Right Shift	  5 >> 1 → 2 (0010)