# ------------------------- Assignment Operators ------------------------

print()  #   =	  Assigns value	            x = 5
print()  #   +=	  Adds and assigns	        x += 3 → x = 8
print()  #   -=	  Subtracts and assigns	    x -= 3 → x = 2
print()  #   *=	  Multiplies and assigns    x *= 3 → x = 15
print()  #   /=	  Divides and assigns	    x /= 2 → x = 2.5
print()  #   //=  Floor divides and assigns	x //= 2 → x = 2
print()  #   %=	  Modulus and assigns	    x %= 2 → x = 1
print()  #   **=  Exponentiates and assigns	x **= 2 → x = 25
print()  #   &=	  Bitwise AND and assigns	x &= 3
print()  #   `	          =`	            Bitwise OR and assigns
print()  #   ^=	  Bitwise XOR and assigns	x ^= 3
print()  #   <<=  Left shift and assigns	x <<= 1
print()  #   >>=  Right shift and assigns	x >>= 1