# ---------------- Comparison Oprators ------------------
# ----- Comparison (Relational) Operators (Return True or False) -----

print(5 == 5)  #   ==	Equal to	    5 == 5 →          True
print(5 != 3)  #   !=   Not equal to	5 != 3 →          True
print(5 > 3)   #   >	Greater than	5 > 3 →           True
print(5 < 3)   #   <	Less than	    5 < 3 →           False
print(5 >= 5)  #   >=   Greater than or equal to 5 >= 5 → True
print(3 <= 5)  #   <=   Less than or equal to 3 <= 5 →    True
